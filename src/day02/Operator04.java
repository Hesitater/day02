package day02;

public class Operator04 {
	public static void main(String[] args) {

		int a = 6;
		int b = 5;

		a += 3; // 9 // a = a + 3
		b = +3; // b =3; 3

		System.out.println(a);
		System.out.println(b);

		// a = 9
		a -= 4; // 5
		a *= 6; // 30
		a /= 3; // 10
		a %= 2; // 0

		System.out.println(a); // 0

	}
}
